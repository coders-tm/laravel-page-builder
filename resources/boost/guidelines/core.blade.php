# Laravel Page Builder

- The Page Builder is a multi-theme, JSON-driven page composition system for Laravel inspired by Shopify's theme architecture. Pages are built from sections and blocks defined in Blade files with `@schema()` directives.
- IMPORTANT: Always use the `search-docs` tool for detailed Page Builder patterns and documentation.
- IMPORTANT: Activate `page-builder-development` skill when working with sections, blocks, themes, page JSON layouts, or any feature of the coderstm/laravel-page-builder package.
